class Stmt2P01TypeDeclarationVer3
{
	public static void main(String[] args) //  method(like function in C)
	{
        /*         
          float height=5.9;	 
		  System.out.println(height); 
  
          float x=0.0; 
         */
        float y=0.1F;  // APPEND (adding at End) with 'F' or 'f'
		System.out.println(y);
		
		float z=0.1f;
		System.out.println(z);
		double a=0.1;  // NO NEED of F or f bcz its already double type
		System.out.println(a);
    }
}