// perfect design using indentation
class IterationStmtForP01DisplayIntegersVer6
{
    public static void  main( String[] args)
	{
		
		for(int a=1,b=5; a<=5;a=a+1,b=b-1) 
			System.out.println(a+"  "+b);
		
		System.out.println();
		
		for(int a=1,b=5; b>=1;a=a+1,b=b-1) 
			System.out.println(a+"  "+b);
		 
	}
}
	